public class IfThen {
    public static void main(String[] args) {
        int mark = 80;
        if (mark >= 80) {
            System.out.println("Well done!");
            System.out.println("Keep it up");
        }
        System.out.println("Life goes on!");

        double temperature = 80.1;
        if (temperature > 80) {
            System.out.println("Too Hot!");
        }
        System.out.println("yummy!");
    }
}